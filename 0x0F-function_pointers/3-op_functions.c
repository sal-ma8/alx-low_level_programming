#include "3-calc.h"

int op_add(int a, int b);
int op_sub(int a, int b);
int op_mul(int a, int b);
int op_div(int a, int b);
int op_mod(int a, int b);

/**
 * op_add - qw
 * @a: num1
 * @b: num2
 * Return: nn
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - qw
 * @a: vp1
 * @b: vp2
 * Return: asd
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - qwed
 * @a: vp2
 * @b: vp1
 * Return: nn
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - vcx
 * @a: vp1
 * @b: vp2
 * Return: nn
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - zxcv
 * @a: vp1
 * @b: vp2
 * Return: nn
 */
int op_mod(int a, int b)
{
	return (a % b);
}
